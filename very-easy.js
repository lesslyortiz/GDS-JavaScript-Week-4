// VERY EASY: Write a function named min that takes two arguments and returns their minimum.
function min(num1, num2) {
    if (num1 < num2){
        return this.num1 + " is the minimum";
    }
    else if (num1 > num2){
        return this.num2 + " is the minimum";
    }
    else {
        return "they're the same!";
    }
}
